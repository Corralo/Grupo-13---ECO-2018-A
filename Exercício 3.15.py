# Tempo perdido de vida devido ao cigarro

cigarros_diários = int(input('Quantos cigarros você fuma por dia?'))
tempo_de_fumo = int(input('Há quantos anos você fuma?'))
tempo_perdido = ((cigarros_diários*10*365*tempo_de_fumo)/(60*24))
print('Você terá %d dias de vida a menos por fumar' %tempo_perdido)


                       
